local Trap = script:GetCustomProperty("Trap"):WaitForObject()
local Jaw = script:GetCustomProperty("Jaw"):WaitForObject()
local JawOffset = script:GetCustomProperty("JawOffset"):WaitForObject()
local SparkVFX = script:GetCustomProperty("SparkVFX"):WaitForObject()
local DustVFX = script:GetCustomProperty("DustVFX"):WaitForObject()
local KlangSFX = script:GetCustomProperty("KlangSFX"):WaitForObject()
local DestroySFX = script:GetCustomProperty("DestroySFX"):WaitForObject()
local Ease3D = require(script:GetCustomProperty("Ease3D"))

Ease3D.EaseRotation(Jaw, Rotation.New(0, 90, 0), .4, Ease3D.EasingEquation.BACK, Ease3D.EasingDirection.INOUT)
Ease3D.EaseRotation(JawOffset, Rotation.New(0, 90, 180), .4, Ease3D.EasingEquation.BACK, Ease3D.EasingDirection.INOUT)
Ease3D.EaseRotation(Trap, Rotation.New(3, 5, 2), .4, Ease3D.EasingEquation.BOUNCE, Ease3D.EasingDirection.OUT)
Task.Wait(.1)
DustVFX:Play()
KlangSFX:Play()
Ease3D.EasePosition(Trap, Vector3.New(0, 0, 50), .2, Ease3D.EasingEquation.BOUNCE, Ease3D.EasingDirection.OUT)
Task.Wait(.2)
Ease3D.EasePosition(Trap, Vector3.New(0, 0, 0), .6, Ease3D.EasingEquation.BOUNCE, Ease3D.EasingDirection.OUT)
Task.Wait(.2)
Ease3D.EaseRotation(Trap, Rotation.New(0, 0, 0), .4, Ease3D.EasingEquation.BOUNCE, Ease3D.EasingDirection.OUT)
Task.Wait(0.2)
SparkVFX:Play()
DestroySFX:Play()
Task.Wait(.2)
Trap:Destroy()