local Fist = script:GetCustomProperty("Fist"):WaitForObject()
local Burst = script:GetCustomProperty("Burst"):WaitForObject()
local Flares = script:GetCustomProperty("Flares"):WaitForObject()
local Debris = script:GetCustomProperty("Debris"):WaitForObject()
local Smoke = script:GetCustomProperty("Smoke"):WaitForObject()
local Ease3D = require(script:GetCustomProperty("Ease3D"))

Ease3D.EasePosition(Fist, Vector3.New(0, 0, 0), .5, Ease3D.EasingEquation.QUINTIC, Ease3D.EasingDirection.IN)
Task.Wait(.5)
Debris:Play()
Burst:Play()
Smoke:Play()
Task.Wait(.5)
Ease3D.EaseRotation(Fist, Rotation.New(0, 0, -60), .5, Ease3D.EasingEquation.BOUNCE, Ease3D.EasingDirection.OUT)
Task.Wait(.5)
Ease3D.EaseRotation(Fist, Rotation.New(0, 0, -900), 1.5, Ease3D.EasingEquation.CUBIC, Ease3D.EasingDirection.IN)
Ease3D.EasePosition(Fist, Vector3.New(0, 0, 200), 1.5, Ease3D.EasingEquation.SINE, Ease3D.EasingDirection.INOUT)
Ease3D.EaseScale(Flares, Vector3.New(.667), .5, Ease3D.EasingEquation.ELASTIC, Ease3D.EasingDirection.OUT)
Task.Wait(.5)
Ease3D.EaseScale(Fist, Vector3.New(1.5), 1, Ease3D.EasingEquation.CUBIC, Ease3D.EasingDirection.INOUT)
Task.Wait(1)
